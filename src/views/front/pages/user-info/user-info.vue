			let res = await this.$api.session(table);
			this.ruleForm = res.data;
			this.tableName = table;
			// 自定义下拉框值
			if(this.tableName=='xueshengyonghu'){
				this.xueshengyonghuxingbieOptions = "男,女".split(',');
